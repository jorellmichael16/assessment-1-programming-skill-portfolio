"""
Some Counting
"""
# Print numbers from 0 to 50 , incrementing by 1
for i in range(0,51,1):
    print(i)
print("")
# Print numbers from 50 to 0 , decrementing by 1
for a in range(50,-1,-1):
    print(a)
print("")

# Print numbers from 30 to 50 , incrementing by 1
for b in range(30,51,1):
    print(b)
print("")
# Print numbers from 50 to 10, decrementing by 2
for c in range(50,8,-2):
    print(c)
print("")
# Print numbers from 100 to 200 (inclusive), incrementing by 5
for d in range(100,205,5):
    print(d)